
class Bitwise {

    public static void main(String[] args) {

        // add number and convert as bitwise operator rule
        System.out.println(5 | 2);
        System.out.println(10 & 2);
        System.out.println(5 ^ 2);
        System.out.println(5 >> 2);
        System.out.println(5 << 2);
        System.out.println(~2);
    }
}
